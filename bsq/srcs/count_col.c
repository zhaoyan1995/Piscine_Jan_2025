#include "bsq.h"

int	count_col(char *buffer2)
{
	int	i;

	buffer2 = skip_first_line(buffer2);
	i = 0;
	while (buffer2[i] != '\0' && buffer2[i] != '\n')
	{	
		i++;
	}
	if (buffer2[i] == '\n')
		i++;
	return (i);
}
