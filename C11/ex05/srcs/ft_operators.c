#include "do_op.h"

int	add(int a, int b)
{
	return (a + b);
}

int	soustraction(int a, int b)
{
	return (a - b);
}

int	multiple(int a, int b)
{
	return (a * b);
}

int	divide(int a, int b)
{
	return (a / b);
}

int	modulo(int a, int b)
{
	return (a % b);
}
